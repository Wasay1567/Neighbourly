-- 4. BOOKINGS & TRANSACTIONS
-- ============================================================================

-- need to add another table with the cancellation details and link it to booking for normalization 
CREATE TABLE bookings (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    service_id RAW(16) NOT NULL,
    seeker_id RAW(16) NOT NULL,
    provider_id RAW(16) NOT NULL,
    booking_reference VARCHAR2(20) UNIQUE NOT NULL,
    scheduled_start TIMESTAMP WITH TIME ZONE NOT NULL,
    scheduled_end TIMESTAMP WITH TIME ZONE NOT NULL,
    actual_start TIMESTAMP WITH TIME ZONE,
    actual_end TIMESTAMP WITH TIME ZONE,
    status VARCHAR2(20) DEFAULT 'pending',
    total_amount NUMBER(10, 2) NOT NULL,
    currency CHAR(3) DEFAULT 'USD',
    special_instructions CLOB,
    confirmed_at TIMESTAMP WITH TIME ZONE,
    completed_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    

    cancellation_reason CLOB,
    cancelled_by RAW(16),
    cancelled_at TIMESTAMP WITH TIME ZONE,
    
    CONSTRAINT booking_status_check CHECK (status IN ('pending', 'confirmed', 'in_progress', 'completed', 'cancelled', 'disputed')),
    CONSTRAINT no_self_book CHECK (seeker_id != provider_id),
    CONSTRAINT valid_sched CHECK (scheduled_end > scheduled_start),
    CONSTRAINT pos_amt CHECK (total_amount >= 0)
);

CREATE TABLE reviews (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    booking_id RAW(16) NOT NULL UNIQUE,
    reviewer_id RAW(16) NOT NULL,
    reviewee_id RAW(16) NOT NULL,
    rating NUMBER NOT NULL,
    title VARCHAR2(150),
    comment_text CLOB,
    response CLOB,
    response_at TIMESTAMP WITH TIME ZONE,
    -- is_anonymous BOOLEAN DEFAULT FALSE,
    -- is_flagged BOOLEAN DEFAULT FALSE,
    -- flagged_reason CLOB,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT valid_rating_val CHECK (rating BETWEEN 1 AND 5),
    CONSTRAINT no_self_rev CHECK (reviewer_id != reviewee_id)
);

CREATE TABLE transactions (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    booking_id RAW(16) NOT NULL,
    payer_id RAW(16) NOT NULL,
    payee_id RAW(16) NOT NULL,
    amount NUMBER(10, 2) NOT NULL,
    currency CHAR(3) DEFAULT 'USD',
    platform_fee NUMBER(10, 2) DEFAULT 0.00,
    net_amount NUMBER(10, 2) NOT NULL,
    status VARCHAR2(20) DEFAULT 'pending',
    payment_method VARCHAR2(50),
    payment_gateway_ref VARCHAR2(255),
    payment_gateway_response JSON,
    processed_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,

    refunded_at TIMESTAMP WITH TIME ZONE,
    refund_reason CLOB,
    
    CONSTRAINT tx_status_check CHECK (status IN ('pending', 'completed', 'failed', 'refunded')),
    CONSTRAINT pos_tx_amt CHECK (amount > 0),
    CONSTRAINT valid_net_amt CHECK (net_amount = amount - platform_fee)
);

CREATE TABLE disputes (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    booking_id RAW(16) NOT NULL,
    raised_by RAW(16) NOT NULL,
    against_user_id RAW(16) NOT NULL,
    category VARCHAR2(100) NOT NULL,
    description CLOB NOT NULL,
    evidence JSON,
    status VARCHAR2(20) DEFAULT 'open',
    assigned_moderator_id RAW(16),
    resolution CLOB,
    resolved_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT dispute_status_check CHECK (status IN ('open', 'under_review', 'resolved', 'closed'))
);

-- ============================================================================
-- 5. PERMISSIONS & CACHE
-- ============================================================================

CREATE TABLE permissions (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) UNIQUE NOT NULL,
    resource VARCHAR2(100) NOT NULL,
    action VARCHAR2(50) NOT NULL,
    description CLOB,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT unique_perm_act UNIQUE (resource, action)
);

CREATE TABLE role_permissions (
    role VARCHAR2(20) NOT NULL,
    permission_id NUMBER NOT NULL,
    granted_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    PRIMARY KEY (role, permission_id),
    CONSTRAINT role_type_check CHECK (role IN ('seeker', 'provider', 'moderator', 'admin'))
);

CREATE TABLE user_permission_overrides (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    user_id RAW(16) NOT NULL,
    permission_id NUMBER NOT NULL,
    is_granted BOOLEAN NOT NULL,
    reason CLOB,
    granted_by RAW(16),
    granted_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMP WITH TIME ZONE,
    
    CONSTRAINT unique_user_perm UNIQUE (user_id, permission_id)
);

CREATE TABLE trending_services_cache (
    neighborhood_id NUMBER NOT NULL,
    service_id RAW(16) NOT NULL,
    trend_score NUMBER(10, 2) NOT NULL,
    period VARCHAR2(20) NOT NULL,
    calculated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    PRIMARY KEY (neighborhood_id, service_id, period)
);

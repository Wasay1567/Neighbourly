CREATE TABLE service_categories (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parent_id NUMBER,
    name VARCHAR2(100) NOT NULL,
    slug VARCHAR2(100) UNIQUE NOT NULL,
    description CLOB,
    icon_url VARCHAR2(500),
    is_active BOOLEAN DEFAULT TRUE,
    sort_order NUMBER DEFAULT 0,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT no_self_ref_cat CHECK (id != parent_id)
);

CREATE TABLE service_listings (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    provider_id RAW(16) NOT NULL,
    category_id NUMBER NOT NULL,
    title VARCHAR2(200) NOT NULL,
    slug VARCHAR2(250) UNIQUE NOT NULL,
    description CLOB NOT NULL,
    short_description VARCHAR2(500),
    price_amount NUMBER(10, 2) NOT NULL,
    price_currency CHAR(3) DEFAULT 'USD',
    price_unit VARCHAR2(50) DEFAULT 'per_hour',
    address_id RAW(16),
    h3_index VARCHAR2(20),
    service_radius_km NUMBER DEFAULT 5,
    duration_minutes NUMBER,
    images JSON DEFAULT '[]',
    tags JSON,
    status VARCHAR2(20) DEFAULT 'draft',
    is_featured BOOLEAN DEFAULT FALSE,
    view_count NUMBER DEFAULT 0,
    booking_count NUMBER DEFAULT 0,
    published_at TIMESTAMP WITH TIME ZONE,
    expires_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    deleted_at TIMESTAMP WITH TIME ZONE,
    
    CONSTRAINT srv_status_check CHECK (status IN ('draft', 'active', 'paused', 'completed', 'archived')),
    CONSTRAINT pos_price CHECK (price_amount >= 0),
    CONSTRAINT pos_duration CHECK (duration_minutes IS NULL OR duration_minutes > 0),
    CONSTRAINT valid_radius CHECK (service_radius_km BETWEEN 1 AND 100)
);

CREATE TABLE service_availability (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    service_id RAW(16) NOT NULL,
    day_of_week NUMBER NOT NULL,
    start_time INTERVAL DAY TO SECOND NOT NULL,
    end_time INTERVAL DAY TO SECOND NOT NULL,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT valid_day CHECK (day_of_week BETWEEN 0 AND 6),
    CONSTRAINT valid_time_range CHECK (end_time > start_time),
    CONSTRAINT unique_availability UNIQUE (service_id, day_of_week, start_time)
);


CREATE TABLE users (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    email VARCHAR2(255) NOT NULL UNIQUE,
    password_hash VARCHAR2(255) NOT NULL,
    phone VARCHAR2(20) UNIQUE,
    role VARCHAR2(20) DEFAULT 'seeker' NOT NULL,
    status VARCHAR2(30) DEFAULT 'pending_verification' NOT NULL,
    email_verified BOOLEAN DEFAULT FALSE,
    phone_verified BOOLEAN DEFAULT FALSE,
    last_login_at TIMESTAMP WITH TIME ZONE,
    failed_login_attempts NUMBER DEFAULT 0,
    locked_until TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    deleted_at TIMESTAMP WITH TIME ZONE,
    
    CONSTRAINT user_role_check CHECK (role IN ('seeker', 'provider', 'moderator', 'admin')),
    CONSTRAINT user_status_check CHECK (status IN ('active', 'suspended', 'deactivated', 'pending_verification')),
    CONSTRAINT email_format CHECK (REGEXP_LIKE(email, '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$')),
    CONSTRAINT phone_format CHECK (phone IS NULL OR REGEXP_LIKE(phone, '^\+?[1-9]\d{1,14}$'))
);

CREATE TABLE user_profiles (
    user_id RAW(16) PRIMARY KEY,
    first_name VARCHAR2(100) NOT NULL,
    last_name VARCHAR2(100) NOT NULL,
    display_name VARCHAR2(150),
    avatar_url VARCHAR2(500),
    bio CLOB,
    date_of_birth DATE,
    verification_status VARCHAR2(20) DEFAULT 'unverified',
    verification_document_url VARCHAR2(500),
    verified_at TIMESTAMP WITH TIME ZONE,
    verified_by RAW(16),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT profile_v_status_check CHECK (verification_status IN ('unverified', 'pending', 'verified', 'rejected')),
    CONSTRAINT age_requirement CHECK (date_of_birth IS NULL OR date_of_birth <= ADD_MONTHS(CURRENT_DATE, -12*18))
);

CREATE TABLE user_reputation (
    user_id RAW(16) PRIMARY KEY,
    total_reviews NUMBER DEFAULT 0,
    average_rating NUMBER(3, 2) DEFAULT 0.00,
    total_bookings_as_provider NUMBER DEFAULT 0,
    -- total_bookings_as_seeker NUMBER DEFAULT 0,
    completed_bookings_as_provider NUMBER DEFAULT 0,
    -- completed_bookings_as_seeker NUMBER DEFAULT 0,
    cancellation_rate NUMBER(5, 2) DEFAULT 0.00,
    response_time_minutes NUMBER,
    reliability_score NUMBER(5, 2) DEFAULT 0.00,
    last_active_at TIMESTAMP WITH TIME ZONE,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT valid_avg_rating CHECK (average_rating BETWEEN 0 AND 5),
    CONSTRAINT valid_cancel_rate CHECK (cancellation_rate BETWEEN 0 AND 100)
);

-- ============================================================================
-- 2. GEOGRAPHY DOMAIN
-- ============================================================================

CREATE TABLE cities (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    state_province VARCHAR2(100),
    country VARCHAR2(100) NOT NULL,
    country_code CHAR(2) NOT NULL,
    timezone VARCHAR2(50) NOT NULL,
    -- is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT unique_city UNIQUE (name, state_province, country)
);

CREATE TABLE neighborhoods (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    city_id NUMBER NOT NULL,
    name VARCHAR2(150) NOT NULL,
    slug VARCHAR2(150),
    description CLOB,
    h3_resolution NUMBER DEFAULT 10,
    h3_cells JSON,
    boundary_geojson JSON,
    is_active BOOLEAN DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT unique_neighborhood UNIQUE (city_id, name)
);

CREATE TABLE addresses (
    id RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
    user_id RAW(16),
    street_address VARCHAR2(255) NOT NULL,
    apartment VARCHAR2(50),
    city_id NUMBER NOT NULL,
    neighborhood_id NUMBER,
    postal_code VARCHAR2(20) NOT NULL,
    latitude NUMBER(10, 8) NOT NULL,
    longitude NUMBER(11, 8) NOT NULL,
    h3_index VARCHAR2(20) NOT NULL,
    h3_resolution NUMBER DEFAULT 9,
    is_primary BOOLEAN DEFAULT FALSE,
    is_verified BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    CONSTRAINT valid_coords CHECK (latitude BETWEEN -90 AND 90 AND longitude BETWEEN -180 AND 180)
);

-- ============================================================================
-- 3. SERVICE DOMAIN
